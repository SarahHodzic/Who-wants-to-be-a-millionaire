function fiftyFifty(){
    
    const dugma = document.querySelectorAll('.answers button');
    let prviNetacan = Math.floor(Math.random() * 4);
    let drugiNetacan;
    
    dugma.forEach((item,index) => {
        if(ongoingQuestion[Object.keys(ongoingQuestion)[index + 1]] === ongoingQuestion.correct){
            while(prviNetacan === index){
                prviNetacan = getRandomNumber(0, 3);}
            do{
                drugiNetacan = getRandomNumber(0, 3);;
            }while(drugiNetacan === prviNetacan || drugiNetacan === index)
            
            dugma[prviNetacan].style.backgroundColor = "red";
            dugma[prviNetacan].style.pointerEvents = "none";
            dugma[drugiNetacan].style.backgroundColor = "red";
            dugma[drugiNetacan].style.pointerEvents = "none";
            return;
        }
    })
    dugmefiftyFifty.src = "./SLIKE/fifty_fifty_used.png";
    dugmefiftyFifty.style.pointerEvents = "none";
    fiftyFiftyActive = true;
}

function getRandomNumber(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}




